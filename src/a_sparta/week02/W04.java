package week02;

public class W04 {
    public static void main(String[] args) {
        // &&(and) , ||(or), !(not)
        boolean flag1 = true;
        boolean flag2 = true;
        boolean flag3 = false;

        System.out.println(flag1);
        System.out.println(flag2);
        System.out.println(flag3);

        System.out.println(flag1||flag2||flag3);

        System.out.println(flag1&&flag2);
        System.out.println(flag1&&flag2&&flag3);

    }
}
