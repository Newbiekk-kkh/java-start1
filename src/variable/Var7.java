package variable;

public class Var7 {
    public static void main(String[] agrs) {
        int a = 100 ; // 변수 선언과 동시에 초기화, int 는 정수
        double b = 10.5 ; // double 은 실수
        boolean c = true; // boolean은 true, false 만 입력가능
        char d = 'A'; // char 은 문자 하나 할당가능
        String e = "hello java"; // String 문자열, 문자열을 다루기위한 특별한 타입

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

    }
}
